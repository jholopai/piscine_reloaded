#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

/*
** Prints out the string given as parameter.
** Allowed functions: ft_purchar.
*/

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}
